/// Izumi App Spacing System
/// Based on 4px base unit
class AppSpacing {
  AppSpacing._();

  // Base Unit
  static const double base = 4.0;

  // Spacing Scale
  static const double xs = 4.0; // base * 1
  static const double sm = 8.0; // base * 2
  static const double md = 12.0; // base * 3
  static const double lg = 16.0; // base * 4
  static const double xl = 20.0; // base * 5
  static const double xxl = 24.0; // base * 6
  static const double xxxl = 32.0; // base * 8
  static const double xxxxl = 40.0; // base * 10

  // Common Paddings
  static const double screenPadding = 16.0;
  static const double cardPadding = 16.0;
  static const double cardMargin = 8.0;
  static const double inputPadding = 12.0;

  // Component Gaps
  static const double buttonGap = 16.0;
  static const double cardGap = 12.0;
  static const double listItemGap = 12.0;
  static const double gridGap = 8.0;

  // Border Radius
  static const double radiusXs = 4.0;
  static const double radiusSm = 8.0;
  static const double radiusMd = 12.0;
  static const double radiusLg = 16.0;
  static const double radiusXl = 20.0;
  static const double radiusFull = 100.0;

  // Component Sizes
  static const double buttonHeight = 48.0;
  static const double buttonHeightSmall = 40.0;
  static const double buttonRound = 56.0;
  static const double buttonRoundLarge = 64.0;
  static const double inputHeight = 48.0;
  static const double otpBoxSize = 56.0;
  static const double navBarHeight = 64.0;
  static const double headerHeight = 56.0;
  static const double iconSizeSm = 20.0;
  static const double iconSize = 24.0;
  static const double iconSizeLg = 32.0;
  static const double avatarSm = 32.0;
  static const double avatarMd = 40.0;
  static const double avatarLg = 96.0;
}
